public class Man {
    private String hakbun;
    private String name;

    public Man() {
    }

    public Man(String hakbun, String name) {
        this.hakbun = hakbun;
        this.name = name;
    }

    public String getHakbun() {
        return hakbun;
    }

    public String getName() {
        return name;
    }

    public void setHakbun(String hakbun) {
        this.hakbun = hakbun;
    }

    public void setName(String name) {
        this.name = name;
    }
}
